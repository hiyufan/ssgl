#!/usr/bin/env python3
"""
批量灌入竞赛知识库数据 — 目标 ~1000 条
分 9 个文档组，覆盖竞赛全生命周期
"""

import json
import time
import requests

API = "http://localhost:8000/ai/api/v1/rag/ingest/batch"
BATCH_SIZE = 20  # 每批提交条数

def ingest_batch(docs):
    resp = requests.post(API, json={"documents": docs}, timeout=120)
    if resp.status_code == 200:
        return True
    else:
        print(f"  ERROR {resp.status_code}: {resp.text[:200]}")
        return False

def run(category, docs):
    """按批次提交一个类别的数据"""
    total = len(docs)
    success = 0
    for i in range(0, total, BATCH_SIZE):
        batch = docs[i:i+BATCH_SIZE]
        if ingest_batch(batch):
            success += len(batch)
            print(f"  [{category}] {success}/{total}")
        else:
            print(f"  [{category}] FAILED at {success}/{total}")
            return success
        time.sleep(0.3)
    return success

